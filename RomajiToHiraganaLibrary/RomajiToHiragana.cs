        /// <summary>
        /// sentenceに含まれているローマ字をひらがなに置換します。
        /// </summary>
        /// <param name="sentence">置換対象文字列</param>
        /// <returns>置換された文字列</returns>
        public static string Convert(string sentence)
        {
            //文字数の多い順に置換する必要がある
            var dictionary = romajiHiraganaPairs.OrderByDescending(x => x.Romaji.Length).ThenBy(x => x.Romaji);

            string temp = sentence;
            foreach (var romajiHiraganaPair in dictionary)
            {
                temp = temp.Replace(romajiHiraganaPair.Romaji, romajiHiraganaPair.Hiragana);
            }

            return temp;
        }
